#include <stdio.h>

// ==================== 함수 선언 (Function Declaration) ====================
// 함수 선언은 함수의 존재를 컴파일러에게 미리 알려주는 역할
// 실제 함수의 내용(정의)은 나중에 작성할 수 있음
void func();         // 입력 없고, 출력 없는 함수 선언
void func1(char);    // 입력은 char형, 출력 없는 함수 선언
int func2(int);      // 입력은 int형, 출력은 int형인 함수 선언
int func3();         // 입력 없고, 출력은 int형인 함수 선언

// ==================== 메인 함수 (프로그램의 시작점) ====================
int main(int arg, char** argv) { // 오버로딩(이름 같으나 입력 다름)
	// 함수 오버로딩: 같은 이름이지만 매개변수(입력)가 다른 함수들을 여러 개 만들 수 있음

	// ==================== 변수 선언 및 초기화 ====================
	char ch = 'a';       // 문자형 변수 ch에 'a' 저장
	int num = 10;        // 정수형 변수 num에 10 저장

	func();			// 함수 호출
	func1(ch);		// 실인자값 
	int res = func2(num); // 출력이 있기 때문에 결과를 받을 변수가 있어야 함! (임시 객체)
	int res2 = func3();
	res += 1;
	return 0;
}

// ==================== 함수 정의 (Function Definition) ====================
// 함수 정의는 함수의 실제 내용(동작)을 구현하는 부분

// 함수 정의 형태: 반환타입 함수이름(매개변수) { 함수 내용 }
void func() {
	// 입력 없고, 출력 없는 함수
	// void: 반환값이 없다는 의미
	printf("Hello\n");   // "Hello" 출력 후 줄바꿈
}
void func1(char arg) {
	// 입력 있고(char형), 출력 없는 함수
	// arg: 형식매개변수(formal parameter) - 함수 호출 시 전달받은 값을 저장하는 변수
	// 함수 호출 시 ch의 값인 'a'가 arg에 복사됨
	printf("%c Hello", arg);  // arg에 저장된 문자를 출력하고 " Hello" 출력
}

int func2(int n) {
	// 입력 있고(int형), 출력 있는(int형) 함수
	// n: 함수 호출 시 전달받은 값을 저장하는 형식매개변수
	// 함수 호출 시 num의 값인 10이 n에 복사됨
	return 0;            // int형 값 0을 반환 (호출한 곳으로 돌려줌)
}

int func3() {	
	// 입력 없고, 출력 있는(int형) 함수
	return 100;
}
// 함수의 정의, 함수의 호출, 함수의 선언

